package org.sid.ebankingbackend.entities;


import jakarta.persistence.*;
import lombok.AllArgsConstructor;
import lombok.Data;
import lombok.NoArgsConstructor;
import org.sid.ebankingbackend.enums.AccountStatus;

import java.util.Date;
import java.util.List;

@Data @AllArgsConstructor @NoArgsConstructor
@Entity
@Inheritance(strategy = InheritanceType.SINGLE_TABLE)
@DiscriminatorColumn(name = "TYPE",length = 4,discriminatorType = DiscriminatorType.STRING)
public abstract class BankAccount {
    @Id
    private String id ;
    private Date createdAt;

    private double balance ;
    //Enregistrer souss forme de String et non pas ordiniry(1,2,3)
    @Enumerated(EnumType.STRING)
    private AccountStatus status;
    @ManyToOne
    private Customer customer;
    //va regarde cette classe il s'agit de la meme attribut
   @OneToMany(mappedBy = "bankAccount",fetch = FetchType.LAZY )
   //LAZY :JUSTE LES INFO DEMANDE PAR DEFAUT
   //EAGER: TOUT LES INFOMATION NECESSAIRE BEACOUP DES DONNEES IL PEUT ETRE DES INFO UNITULE
    private List<AccountOperation> accountOperations;
}




















































































































































































































































































































